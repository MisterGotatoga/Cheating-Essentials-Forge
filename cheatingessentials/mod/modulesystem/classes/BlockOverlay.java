	public void drawESP(double x, double y, double z, float f)
    {
    	Minecraft.getMinecraft().entityRenderer.disableLightmap(f);
        drawESP(x - RenderManager.renderPosX, y - RenderManager.renderPosY, z - RenderManager.renderPosZ, 1.0F, 0.0F, 1.0F, Wrapper.INSTANCE.player(), Wrapper.INSTANCE.minecraft().objectMouseOver);
        Minecraft.getMinecraft().entityRenderer.enableLightmap(f);
    }
   
    public void drawESP(double d, double d1, double d2, double r, double b, double g, EntityPlayer player, MovingObjectPosition position)
    {
    	GL11.glPushMatrix();
        GL11.glEnable(3042);
        GL11.glBlendFunc(770, 771);
        GL11.glLineWidth(1.0F);
        GL11.glDisable(GL11.GL_LIGHTING);
        GL11.glDisable(GL11.GL_TEXTURE_2D);
        GL11.glEnable(GL11.GL_LINE_SMOOTH);
        GL11.glDisable(2929);
        GL11.glDepthMask(false);

        double blockX = player.lastTickPosX - (player.posX - player.lastTickPosX);
        double blockY = player.lastTickPosY - (player.posY - player.lastTickPosY);
        double blockZ = player.lastTickPosZ - (player.posZ - player.lastTickPosZ);
        Block block = Wrapper.INSTANCE.world().func_147439_a(position.blockX, position.blockY, position.blockZ);
        GL11.glColor4f(0.0F, 0.0F, 0.0F, 0.2F);
        drawOutlinedBoundingBox(block.func_149633_g(Wrapper.INSTANCE.world(), position.blockX, position.blockY, position.blockZ).expand((double)0.002, (double)0.002, (double)0.002).getOffsetBoundingBox(-blockX, -blockY, -blockZ));
        GL11.glColor4f(0.0F, 0.4F, 0.0F, 0.2F);
        drawBoundingBox(block.func_149633_g(Wrapper.INSTANCE.world(), position.blockX, position.blockY, position.blockZ).expand((double)0.002, (double)0.002, (double)0.002).getOffsetBoundingBox(-blockX, -blockY, -blockZ));
        
        GL11.glLineWidth(1F);
        GL11.glDisable(GL11.GL_LINE_SMOOTH);
        GL11.glEnable(GL11.GL_TEXTURE_2D);
        GL11.glEnable(GL11.GL_LIGHTING);
        GL11.glEnable(2929);
        GL11.glDepthMask(true);
        GL11.glDisable(3042);
        GL11.glPopMatrix();
    }
   
